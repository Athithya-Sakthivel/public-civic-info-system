	function altmsg() {
    var e = window.event;
    var target = e.target || e.srcElement;

    // Traverse to anchor tag
    while (target && target.tagName.toUpperCase() !== 'A') {
        target = target.parentElement;
    }

    if (target && target.href) {
        var linkHost = new URL(target.href).hostname;

        // Skip alert if link is to cms.tn.gov.in
        if (linkHost.endsWith('cms.tn.gov.in')) {
            return;
        }

        // Show alert for other domains
        if (!confirm('You are being redirected to an External Website.')) {
            e.preventDefault();
            e.stopPropagation();
        }
    }
}

