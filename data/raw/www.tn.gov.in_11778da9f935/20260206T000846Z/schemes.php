<script>
  $(function () {
    // Hide all nested lists initially
    $('.site-map ul').hide();
    // Show only the top-level list
    $('.site-map > ul').show();
    // Show any pre-activated lists
    $('.site-map ul.active').show();

    $('.site-map li').on('click', function (e) {
        // Prevent toggling for specific parent links like "Menu"
        if ($(this).children('a').text().trim() === 'Menu') {
            e.stopPropagation();
            return;
        }

        var children = $(this).find('> ul'); // Immediate child list

        // Close any currently open sibling lists and their descendants
        $(this).siblings().find('ul:visible').hide('fast').removeClass('active');

        // Toggle the visibility of the clicked list and close all child nodes
        if (children.is(":visible")) {
            children.hide('fast').removeClass('active'); // Close current
        } else {
            children.show('fast').addClass('active'); // Open current
        }

        e.stopPropagation(); // Prevent event bubbling
    });
});


    </script>





    
