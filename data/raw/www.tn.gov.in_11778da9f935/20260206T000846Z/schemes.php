<script>
 const audio = document.getElementById('persistentAudio');
const playButton = document.querySelector('.play-button');
const durationElement = document.querySelector('.duration');
let isPlaying = false;

// Toggle play/pause functionality
const toggleAudio = () => {
  if (isPlaying) {
    audio.pause();
    isPlaying = false;
    playButton.classList.remove('playing');
  } else {
    audio.play();
    isPlaying = true;
    playButton.classList.add('playing');
  }
  localStorage.setItem('isPlaying', isPlaying);
};

// Update the duration display while the audio is playing
audio.addEventListener('timeupdate', () => {
  const minutes = Math.floor(audio.currentTime / 60);
  const seconds = Math.floor(audio.currentTime - minutes * 60);
  durationElement.innerHTML = `${minutes}:${seconds < 10 ? '0' + seconds : seconds}`;
});

// Save current time to localStorage
audio.addEventListener('timeupdate', () => {
  localStorage.setItem('audioTime', audio.currentTime);
});

// Load audio state and time on page load
window.onload = () => {
  const savedTime = localStorage.getItem('audioTime');
  if (savedTime) {
    audio.currentTime = parseFloat(savedTime);
  }

  const savedState = localStorage.getItem('isPlaying') === 'true';
  if (savedState) {
    audio.play();
    playButton.classList.add('playing');
    isPlaying = true;
  }
};

// Reset the play button when the audio finishes, without changing the text
audio.addEventListener('ended', () => {
  isPlaying = false;
  playButton.classList.remove('playing');
  // Do not change the text of the button
  // playButton.innerText = "Play"; // This line is removed to keep the original text
  // Ensure duration remains visible after the audio ends
  durationElement.style.display = "inline";
});

// Save state on unload (in case navigation is about to happen)
window.onbeforeunload = () => {
  localStorage.setItem('audioTime', audio.currentTime);
  localStorage.setItem('isPlaying', isPlaying);
};

</script>


